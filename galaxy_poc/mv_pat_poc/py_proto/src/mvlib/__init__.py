## @mainpage MOA Library Documentation
#
# This is the documentation of the MOA Library, a Python
# 2.7 library that implements a Part Average Testing method
# using a Multivariate Outlier Analysis and makes it possible
# to experiment with semiconductor test data.
# <BR>
# The library provides a set of classes and functions grouped
# into modules to manage the input data (in CSV files), create
# a multivariate PAT recipes (in JSON files), apply the recipes
# and generate results (in CSV and Binary files) and visualize
# the results graphically.
# <BR><BR>
# See the list of modules for more details.
